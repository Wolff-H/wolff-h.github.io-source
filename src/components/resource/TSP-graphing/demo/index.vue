        <el-button class="button to-project-report"
            size="small"
            @click="jumpToProjectReport()"
        >
            project report
        </el-button>
        <div class="main">
            <el-tabs class="tabs"
                type="border-card"
            >
                <el-tab-pane class="pane experiment"
                    :style="if_show_dashboard ? 'width: calc(100% - 400px);' : 'width: initial;'"
                    label="Experiment"
                >
                    <div>
                        <div class="title">Time Cost</div>
                        <div class="subtitle">time cost of each trial</div>
                        <div class="figure time-cost"
                            ref="figure_time_cost"
                        ></div>
                    </div>
                    <div class="divbar"></div>
                    <div>
                        <div class="title">Result</div>
                        <div class="subtitle">based on each generation's max、avg、min distance</div>
                        <div class="figure result"
                            ref="figure_result"
                        ></div>
                    </div>
                </el-tab-pane>
                <el-tab-pane class="pane evolution"
                    :style="if_show_dashboard ? 'width: calc(100% - 400px);' : 'width: initial;'"
                    label="Evolution"
                >
                    <div class="title">Distance Trend</div>
                    <div class="subtitle">based on each generation's max、avg、min distance</div>
                    <div class="figure evolution"
                        ref="figure_evolution"
                    ></div>
                </el-tab-pane>
                <el-tab-pane class="pane generation"
                    :style="if_show_dashboard ? 'width: calc(100% - 400px);' : 'width: initial;'"
                    label="Generation"
                >
                    <div class="figure generation"
                        ref="figure_generation"
                    ></div>
                </el-tab-pane>
                <el-tab-pane class="pane individual"
                    :style="if_show_dashboard ? 'width: calc(100% - 400px);' : 'width: initial;'"
