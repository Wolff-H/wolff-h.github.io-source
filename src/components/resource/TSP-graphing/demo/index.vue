<script lang="ts">
    import coords_WesternSahara from '../data/coords_WesternSahara'
    import coords_Uruguay from '../data/coords_Uruguay'
    // import coords_Canada from '../data/coords_Canada'
    import DrawingModule from './DrawingModule'

    import Vue from 'vue'

    export default Vue.extend
    ({
        components: 
        {
            
        },
        data()
        {
            return{
                using_instance: '',
                using_algorithm: 'advanced',

                if_show_dashboard: true,

                trial_n: 1,
                trial_all: 20,

                generation_n: 1,
                generation_all: 100,

                individual_n: 1,

                using_route: '',

                datasets:
                {
                    basic:
                    {
                        WesternSahara: null,
                        Uruguay: null,
                        Canada: null,
                    },
                    advanced:
                    {
                        WesternSahara: null,
                        Uruguay: null,
                        Canada: null,
                    }
                },

                using_generation_avg_distance: 0,
                using_generation_best_distance: 0,
                using_generation_worst_distance: 0,

                plain_map:
                {
                    WesternSahara: [[0],[0],[0]],
                    Uruguay: [[0],[0],[0]],
                    Canada: [[0],[0],[0]],
                }

            }
        },
        methods:
        {
            toggleDashboard() :void
            {
                this.if_show_dashboard = !this.if_show_dashboard
                console.log(this.datasets[this.using_algorithm].WesternSahara)
                console.log(this.datasets[this.using_algorithm].Uruguay)
            },

            // use instance //
            useInstance()
            {
                switch(this.using_instance)
                {
                    case 'WesternSahara':
                        // draw plain map ------------------------------------------------------------------------------
                        DrawingModule.drawPlainMap(coords_WesternSahara, this.$refs.figure_plain_map)
                        // fetch target data ---------------------------------------------------------------------------
                        if(this.datasets[this.using_algorithm].WesternSahara == null)
                        {
                            DrawingModule.fetchData(this.using_algorithm + '_WesternSahara')
                            .then
                            (
                                (response:any) =>
                                {
                                    if(response.data != 'no such dataset')
                                    {
                                        this.$notify({title: 'data fetched successfully', message: ''})

                                        this.datasets[this.using_algorithm].WesternSahara = response.data

                                        this.drawWesternSahara()
                                    }
                                    else
                                    {
                                        this.$notify(
                                            {
                                                title: 'data unavailable',
                                                message: `Server responded: ${'no such dataset'}`,
                                            }
                                        )
                                    }
                                }
                            )
                        }
                        // while has data, draw ------------------------------------------------------------------------
                        else
                        {
                            this.drawWesternSahara()
                        }
                        break
                    case 'Uruguay':
                        // draw plain map ------------------------------------------------------------------------------
                        DrawingModule.drawPlainMap(coords_Uruguay, this.$refs.figure_plain_map)
                        // fetch target data ---------------------------------------------------------------------------
                        if(this.datasets[this.using_algorithm].Uruguay == null)
                        {
                            DrawingModule.fetchData(this.using_algorithm + '_Uruguay')
                            .then
                            (
                                (response:any) =>
                                {
                                    if(response.data != 'no such dataset')
                                    {
                                        this.$notify({title: 'data fetched successfully', message: ''})

                                        this.datasets[this.using_algorithm].Uruguay = response.data

                                        DrawingModule.drawExperiment(
                                            this.datasets[this.using_algorithm].Uruguay,
                                            this.generation_all,
                                            this.$refs.figure_time_cost, 
                                            this.$refs.figure_result
                                        )
                                    }
                                    else
                                    {
                                        this.$notify({title: 'data unavailable', message: ''})
                                    }
                                }
                            )
                        }
                        else
                        {
                            // draw experiment - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
                            DrawingModule.drawExperiment(
                                this.datasets[this.using_algorithm].Uruguay,
                                this.generation_all,
                                this.$refs.figure_time_cost, 
                                this.$refs.figure_result
                            )
                        }
                        break
                    case 'Canada':
                        // DrawingModule.drawPlainMap(coords_Canada, this.$refs.figure_plain_map)
                        // Canada dataset is abandoned as it's way too big //
                        break
                    default:
                        break
                }
            },

            jumpToGithubRepo() :void
            {
                window.open('https://github.com/Wolff-H/COMP-3-201', '_blank')
            },

            jumpToProjectReport() :void
            {
                window.open('https://github.com/Wolff-H/COMP-3-201/blob/master/Project/COMP3201FinalProjectReport.pdf', '_blank')
            },

            // 0 //
            drawExperiment()
            {
                // draw experiment - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
                DrawingModule.drawExperiment(
                    this.datasets[this.using_algorithm].WesternSahara,
                    this.generation_all,
                    this.$refs.figure_time_cost, 
                    this.$refs.figure_result,
                )
            },
            drawEvolution()
            {
                // draw evolution - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
                DrawingModule.drawEvolution(
                    this.datasets[this.using_algorithm].WesternSahara,
                    this.$refs.figure_evolution,
                    this.generation_all,
                    this.trial_n,
                )
            },
            drawGeneration()
            {
                // draw generation - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
                let dashboard_data = 
                DrawingModule.drawGeneration(
                    this.datasets[this.using_algorithm].WesternSahara,
                    this.$refs.figure_generation,
                    this.generation_all,
                    this.trial_n,
                    this.generation_n,
                )
                this.using_generation_avg_distance = dashboard_data.avg
                this.using_generation_best_distance = dashboard_data.best
                this.using_generation_worst_distance = dashboard_data.worst
            },
            drawIndividual()
            {
                // draw individual - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
                this.using_route =
                DrawingModule.drawIndividual(
                    this.datasets[this.using_algorithm].WesternSahara,
                    this.$refs.figure_individual,
                    this.generation_all,
                    this.trial_n,
                    this.generation_n,
                    this.individual_n,
                    this.plain_map.WesternSahara,
                )
            },


            // 1 //
            drawWesternSahara()
            {
                this.drawExperiment()
                this.drawEvolution()
                this.drawGeneration()
                this.drawIndividual()
            }
        },
        mounted()
        {
            DrawingModule.getTestJsonFile()
            this.plain_map.WesternSahara = DrawingModule.parseCoords(coords_WesternSahara)

            // automatically fetched the default dataset //
            this.$nextTick(
                () =>
                {
                    this.using_instance = 'WesternSahara'
                    this.useInstance()
                }
            )
        },
    })
</script>



